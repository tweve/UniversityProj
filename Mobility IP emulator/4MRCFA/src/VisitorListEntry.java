
public class VisitorListEntry {

		String MN;
		String MNHoA;
		String Mac;
		int TTL;
		boolean temp;
		
		public String getMN() {
			return MN;
		}
		public void setMN(String mN) {
			MN = mN;
		}
		public String getMNHoA() {
			return MNHoA;
		}
		public void setMNHoA(String mNHoA) {
			MNHoA = mNHoA;
		}
		public String getMac() {
			return Mac;
		}
		public void setMac(String mac) {
			Mac = mac;
		}
		public int getTTL() {
			return TTL;
		}
		public void setTTL(int tTL) {
			TTL = tTL;
		}
		public boolean isTemp() {
			return temp;
		}
		public void setTemp(boolean temp) {
			this.temp = temp;
		}
		
		
}
